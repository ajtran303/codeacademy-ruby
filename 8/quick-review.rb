Quick review

1. A block can be passed into methods but is not an object
1a. The syntax is do..end or {}

2. A proc is a saved block that IS an object
2a. Procs do not care about how many args are passed
2b. Procs do not return control to the calling method

3. A lambda is like a proc but differs in two ways
3a. Lambdas specify a specific number of arguments
3b. Lambdas will return control to the calling method

